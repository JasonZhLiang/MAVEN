package com.linguaclassica.model;

import java.io.Serializable;
import java.sql.Timestamp;

/**
 * Business layer interface for ConsultantClientsModel corresponding 
 * to the consultant_clients table.
 * @author Rob Hilchie
 */
public interface ConsultantClientsModel extends Serializable
{
	/**
	 * Gets the id
	 * @return
	 */
	public Integer getId();

	/**
	 * Gets the organization id
	 * @return
	 */
	public Integer getOrganizationId();

	/**
	 * Sets the organization id
	 * @param organizationId
	 */
	public void setOrganizationId(Integer organizationId);

	/**
	 * Gets the consultant id
	 * @return
	 */
	public Integer getConsultantId();

	/**
	 * Sets the consultant id
	 * @param consultantId
	 */
	public void setConsultantId(Integer consultantId);

	/**
	 * Set the client id
	 * @param clientId
	 */
	public void setClientId(Integer clientId);
	
	/**
	 * Get the client id
	 * @return
	 */
	public Integer getClientId();

	Timestamp getClosedtime();

	void setClosedtime(Timestamp closedtime);
}
