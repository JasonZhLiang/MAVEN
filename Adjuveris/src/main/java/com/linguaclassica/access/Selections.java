package com.linguaclassica.access;

import java.io.Serializable;

public class Selections implements Serializable {
	private static final long serialVersionUID = 1L;
	
	String selectedVar1;
	String selectedVar2;
	String exerciseurl;
	int variables;
	
	public Selections() {
		
	}
	
	public Selections(String selectedVar1, String selectedVar2, 
			String exerciseurl, int variables) {
		this.selectedVar1 = selectedVar1;
		this.selectedVar2 = selectedVar2;
		this.exerciseurl = exerciseurl;
		this.variables = variables;
	}	
	

	/**
     * @return Currently selected var1
     */
    public String getSelectedVar1()
    {
        return selectedVar1;
    }

    /**
     * @param selectedVar1
     *            The make that is currently selected
     */
    public void setSelectedVar1(String selectedVar1)
    {
        this.selectedVar1 = selectedVar1;
    }

	/**
     * @return Currently selected var2
     */
    public String getSelectedVar2()
    {
        return selectedVar2;
    }

    /**
     * @param selectedVar2
     *            The make that is currently selected
     */
    public void setSelectedVar2(String selectedVar2)
    {
        this.selectedVar2 = selectedVar2;
    }

	/**
     * @return exerciseurl
     */
    public String getExerciseurl()
    {
        return exerciseurl;
    }

    /**
     * @param exerciseurl
     *            
     */
    public void setExerciseurl(String exerciseurl)
    {
        this.exerciseurl = exerciseurl;
    }

	/**
     * @return variables
     */
    public int getVariables()
    {
        return variables;
    }

    /**
     * @param exerciseurl
     *            
     */
    public void setVariables(int variables)
    {
        this.variables = variables;
    }
}