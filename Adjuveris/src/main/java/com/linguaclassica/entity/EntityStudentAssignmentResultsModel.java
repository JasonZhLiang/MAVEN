package com.linguaclassica.entity;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

import com.linguaclassica.model.StudentAssignmentResultsModel;

/**
 * Entity implementation of the StudentAssignmentResultsModel interface
 * @author Kwirtanen
 */

@Entity
@Table(name="student_assignment_results")
public class EntityStudentAssignmentResultsModel implements StudentAssignmentResultsModel{

	private static final long serialVersionUID = 1L;
	
	@Id
	@GeneratedValue()
	private Integer id;
	
	private Integer assignmentFactId;
	private Integer correctNumber;
	private Float percent;
	private Integer total;
	private Integer inflectcnt;
	private Integer inflectright;
	private Float inflectpct;
	private Integer syntaxcnt;
	private Integer syntaxright;
	private Float syntaxpct;
	private Integer vocabcnt;
	private Integer vocabright;
	private Float vocabpct;
	private Integer comprehencnt;
	private Integer comprehenright;
	private Float comprehenpct;
	
	public EntityStudentAssignmentResultsModel(){
	}
	
	public EntityStudentAssignmentResultsModel(Integer assignmentFactId,
			Integer correctNumber,
			Float percent,
			Integer total,
			Integer inflectcnt,
			Integer inflectright,
			Float inflectpct,
			Integer syntaxcnt,
			Integer syntaxright,
			Float syntaxpct,
			Integer vocabcnt,
			Integer vocabright,
			Float vocabpct,
			Integer comprehencnt,
			Integer comprehenright,
			Float comprehenpct){
		this.assignmentFactId = assignmentFactId;
		this.correctNumber = correctNumber;
		this.percent = percent;
		this.total = total;
		this.inflectcnt = inflectcnt;
		this.inflectright = inflectright;
		this.inflectpct = inflectpct;
		this.syntaxcnt = syntaxcnt;
		this.syntaxright = syntaxright;
		this.syntaxpct = syntaxpct;
		this.vocabcnt = vocabcnt;
		this.vocabright = vocabright;
		this.vocabpct = vocabpct;
		this.comprehencnt = comprehencnt;
		this.comprehenright = comprehenright;
		this.comprehenpct = comprehenpct;
		
	}
	
	@Override
	public Integer getId() {
		return id;
	}

	@Override
	public Integer getAssignmentFactId() {
		return assignmentFactId;
	}

	@Override
	public void setAssignmentFactId(Integer assignmentFactId) {
		this.assignmentFactId = assignmentFactId;
		
	}

	@Override
	public Integer getCorrectNumber() {
		return correctNumber;
	}

	@Override
	public void setCorrectNumber(Integer correctNumber) {
		this.correctNumber = correctNumber;
		
	}

	@Override
	public Float getPercent() {
		return percent;
	}

	@Override
	public void setPercent(Float percent) {
		this.percent = percent;
		
	}

	@Override
	public Integer getTotal() {
		return total;
	}

	@Override
	public void setTotal(Integer total) {
		this.total = total;
		
	}

	@Override
	public Integer getInflectcnt() {
		return inflectcnt;
	}

	@Override
	public void setInflectcnt(Integer inflectcnt) {
		this.inflectcnt = inflectcnt;
		
	}

	@Override
	public Integer getInflectright() {
		return inflectright;
	}

	@Override
	public void setInflectright(Integer inflectright) {
		this.inflectright = inflectright;
		
	}

	@Override
	public Float getInflectpct() {
		return inflectpct;
	}

	@Override
	public void setInflectpct(Float inflectpct) {
		this.inflectpct = inflectpct;
		
	}

	@Override
	public Integer getSyntaxcnt() {
		return syntaxcnt;
	}

	@Override
	public void setSyntaxcnt(Integer syntaxcnt) {
		this.syntaxcnt = syntaxcnt;
		
	}

	@Override
	public Integer getSyntaxright() {
		return syntaxright;
	}

	@Override
	public void setSyntaxright(Integer syntaxright) {
		this.syntaxright = syntaxright;
		
	}

	@Override
	public Float getSyntaxpct() {
		return syntaxpct;
	}

	@Override
	public void setSyntaxpct(Float syntaxpct) {
		this.syntaxpct = syntaxpct;
		
	}

	@Override
	public Integer getVocabcnt() {
		return vocabcnt;
	}

	@Override
	public void setVocabcnt(Integer vocabcnt) {
		this.vocabcnt = vocabcnt;
		
	}

	@Override
	public Integer getVocabright() {
		return vocabright;
	}

	@Override
	public void setVocabright(Integer vocabright) {
		this.vocabright = vocabright;
		
	}

	@Override
	public Float getVocabpct() {
		return vocabpct;
	}

	@Override
	public void setVocabpct(Float vocabpct) {
		this.vocabpct = vocabpct;
		
	}

	@Override
	public Integer getComprehencnt() {
		return comprehencnt;
	}

	@Override
	public void setComprehencnt(Integer comprehencnt) {
		this.comprehencnt = comprehencnt;
		
	}

	@Override
	public Integer getComprehenright() {
		return comprehenright;
	}

	@Override
	public void setComprehenright(Integer comprehenright) {
		this.comprehenright = comprehenright;
		
	}

	@Override
	public Float getComprehenpct() {
		return comprehenpct;
	}

	@Override
	public void setComprehenpct(Float comprehenpct) {
		this.comprehenpct = comprehenpct;
		
	}
}
