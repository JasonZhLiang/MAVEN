package com.linguaclassica.model;

import java.io.Serializable;


/**
 * Business layer interface for InstUsersModel corresponding 
 * to the inst_users look-up table.
 * @author Gene Price
 */
public interface InstUsersModel extends Serializable
{
	
	/**
	 * Gets the id
	 * @return
	 */
	public Integer getId();

	/**
	 * Gets the class id
	 * @return
	 */
	public Integer getInstid();

	/**
	 * Sets the class id
	 * @param classId
	 */
	public void setInstid(Integer instid);

	/**
	 * Gets the user id
	 * @return
	 */
	public Integer getUserid();

	/**
	 * Sets the user id
	 * @param userId
	 */
	public void setUserid(Integer userid);
}
