package com.linguaclassica.model;

import java.io.Serializable;

/**
 * Business layer interface for EntityGroupModel entity.
 */
public interface GroupModel extends Serializable {
	
	/**
	 * Get the unique id of the group
	 * @return group id
	 */
	public Integer getId();
	
	/**
	 * Set the unique id of the group
	 * @param id
	 */
	void setId(Integer id);
	
	/**
	 * Get the name of the group
	 * @return group name
	 */
	String getGroupName();
	
	/**
	 * Set the name of the group
	 * @param groupName
	 */
	void setGroupName(String groupName);
	
	/**
	 * Get the user id that created the group
	 * @return user id
	 */
	Integer getCreatedById();
	
	/**
	 * Set the user id that created the group
	 * @param createdById
	 */
	void setCreatedById(Integer createdById);

}
