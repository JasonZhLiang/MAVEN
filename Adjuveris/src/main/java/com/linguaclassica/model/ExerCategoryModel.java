package com.linguaclassica.model;

import java.io.Serializable;


/**
 * Business layer interface for EntityExerciseModel entity.
 */
public interface ExerCategoryModel extends Serializable {
	
	/**
	 * Returns the unique ID field for this permission model. 
	 * @return id
	 */
	public Integer getId();	

	/**
	 * Returns the exercisename
	 * @return exercisename
	 */
	String getExerciseName();

	/**
	 * Sets the exercise name
	 * @param exercisename
	 */
	void setExerciseName(String exercisename);	

	/**
	 * Returns the urlbase to which variables will be added
	 * @return urlbase
	 */
	String getUrlbase();

	/**
	 * Sets the urlbase to which variables will be added
	 * @param urlbase
	 */
	void setUrlbase(String urlbase);

	String getExercisetype();

	void setExercisetype(String exercisetype);	

	/**
	 * Returns the comment explaining the content and method of the exercise
	 * @return comment
	 */
	String getComment();

	/**
	 * Sets the comment
	 * @param comment
	 */
	void setComment(String comment);

}

