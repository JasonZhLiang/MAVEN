package com.linguaclassica.model;

import java.io.Serializable;


/**
 * Business layer interface for class users model corresponding 
 * to the class_users look-up table.
 * @author Tammy
 */
public interface ClassUsersModel extends Serializable
{

	/**
	 * Gets the id
	 * @return
	 */
	public Integer getId();
	
	/**
	 * Sets the id
	 * @param id
	 */
	public void setId(Integer id);

	/**
	 * Gets the class id
	 * @return
	 */
	public Integer getClassId();

	/**
	 * Sets the class id
	 * @param classId
	 */
	public void setClassId(Integer classId);

	/**
	 * Gets the user id
	 * @return
	 */
	public Integer getUserId();

	/**
	 * Sets the user id
	 * @param userId
	 */
	public void setUserId(Integer userId);
	
	/**
	 * Returns the UserType of classuser
	 * @return
	 */
	public String getUserType();
	
	
	/**
	 * Sets the UserType for classuser either STUDENT or TEACHER
	 * @param usertype
	 */
	public void setUserType(String usertype);

}
